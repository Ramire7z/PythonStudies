import random


def jogar():
    imprimir_mensagem_de_abertura()

    # Seleção aleatória de palavra secreta

    palavra_secreta = palavra_secreta_randomizada()

    # Função para esconder as letras das palavras, que serão reveladas gradualmente.

    letras_acertadas = segredo_letras(palavra_secreta)
    print(letras_acertadas)

    enforcou = False
    acertou = False
    tentativas = 0

    # enquanto(True)
    while not enforcou and not acertou:

        chute = solicitar_chute()

        if chute in palavra_secreta:
            sinaliza_chute_correto(chute, letras_acertadas, palavra_secreta)
        else:
            tentativas += 1
            desenha_forca(tentativas)

        enforcou = tentativas == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if acertou:
        imprime_mensagem_vencedor()
    else:
        imprime_mensagem_perdedor(palavra_secreta)

def imprimir_mensagem_de_abertura():
    print("***************************")
    print("Bem vindo ao jogo de Forca!")
    print("***************************")


def palavra_secreta_randomizada():
    with open("palavras.txt") as arquivo:
        palavras = []

        for linha in arquivo:
            linha = linha.strip()
            palavras.append(linha)

    numero = random.randrange(0, len(palavras))
    palavra_secreta = palavras[numero].upper()
    return palavra_secreta

def segredo_letras(palavra_secreta):
    return ["_" for letra in palavra_secreta]

def solicitar_chute():
    chute = input("Qual letra? ")
    chute = chute.strip().upper()
    return chute

def sinaliza_chute_correto(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if chute == letra:
            letras_acertadas[index] = letra
        index += 1

